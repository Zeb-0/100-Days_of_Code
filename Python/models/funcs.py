#!/usr/bin/python3

''' module containing functions '''

class Funcs:
    
    def add(self, a, b):
        return a + b

    def minus(self, a, b):
        return b - a

    def mul(self, a, b):
        return a * b

    def square(self, a, b):
        return a ** b

    def div(self, a, b):
        return a / b

